int	ft_atoi(char *str)
{
	int	i;
	int	n;

	i = 0;
	n = 0;
	while ((*str >= 9 && *str <= 13) || (*str == ' '))
		str++;
	while (*str == '-' || *str == '+')
	{
		if (*str == '-')
			n++;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		i = i * 10 + *str - '0';
		str++;
	}
	if ((n % 2) == 1)
		return (-i);
	return (i);
}
